package com.example.elearningplus;

public class Profile_DiemSV {
    String tenMH;
    float diemMH;

    public Profile_DiemSV(String tenMH, float diemMH) {
        this.tenMH = tenMH;
        this.diemMH = diemMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public float getDiemMH() {
        return diemMH;
    }

    public void setDiemMH(float diemMH) {
        this.diemMH = diemMH;
    }
}
